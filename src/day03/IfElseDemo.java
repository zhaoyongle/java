package day03;

/*
 * if....else语句结构
 * 语法：
 * 	if(条件){
 * 		if语句的结构体
 * }else{
 * 		else语句结构体
 * }
 */

public class IfElseDemo {
	public static void main(String[] args){
		int a = 10;
		
		if(a>10){
			System.out.println("a>10");
		}else{
			System.out.println("a不大于10");
		}
	}

}
